package collection13feb;

import java.util.Arrays;

public class Employee {
	
	int id;
	String name;
	String role;
	float sal;
	public Employee(int id, String name, String role, float sal) {
		super();
		this.id = id;
		this.name = name;
		this.role = role;
		this.sal = sal;
	}
	@Override
	public String toString() {
		return "Student2 [id=" + id + ", name=" + name + ", role=" + role + ", sal=" + sal + "]";
	}
	
	
	
}
	    
	   
